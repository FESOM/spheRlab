sl.proj.mollweide <-
  function (lon, lat) {
    x = lon * (9.998196311e-01 - 8.425176851e-05 * lat^2 - 4.990552181e-08 * lat^4 + 9.052780240e-11 * lat^6
               - 8.271958698e-14 * lat^8 + 4.274458599e-17 * lat^10 - 1.331239390e-20 * lat^12 + 2.546227958e-24 * lat^14
               - 2.924874154e-28 * lat^16 + 1.850660684e-32 * lat^18 - 4.955803495e-37 * lat^20)
    y = 1.228170817 * lat - 1.918177315e-05 * lat^3 - 1.072147210e-09 * lat^5
    return(list(x=x,y=y))
}